    <script>
        document.addEventListener('DOMContentLoaded', () => {

            const inputBuscar = document.getElementById('inputBuscar');
            const formBuscar = document.getElementById('formBuscar');
            const tabla = document.getElementById('tabla-representantes');
            const loader = document.getElementById('loaderTablaRepresentantes');

            let debounceTimer = null;

            function mostrarLoader() {
                loader.style.display = 'flex';
                tabla.style.opacity = '0.5'; // feedback visual extra
            }

            function ocultarLoader() {
                loader.style.display = 'none';
                tabla.style.opacity = '1';
            }

            async function cargarTabla(buscar = '', page = 1) {
                // 🔥 SIEMPRE se ejecuta
                mostrarLoader();

                const url =
                    `<?php echo e(route('representante.index')); ?>?buscarpor=${encodeURIComponent(buscar)}&page=${page}`;

                try {
                    const response = await fetch(url, {
                        headers: {
                            'X-Requested-With': 'XMLHttpRequest'
                        }
                    });

                    if (!response.ok) throw new Error('Error en la respuesta');

                    const html = await response.text();
                    tabla.innerHTML = html;

                } catch (error) {
                    console.error(error);
                    alert('Error al cargar los datos');
                } finally {
                    // 🔥 SOLO se oculta cuando TODO terminó
                    ocultarLoader();
                }
            }

            // 🔎 Buscar (debounce)
            inputBuscar.addEventListener('input', () => {
                clearTimeout(debounceTimer);
                debounceTimer = setTimeout(() => {
                    cargarTabla(inputBuscar.value.trim(), 1);
                }, 400);
            });

            // 📝 Submit
            formBuscar.addEventListener('submit', (e) => {
                e.preventDefault();
                cargarTabla(inputBuscar.value.trim(), 1);
            });

            // 📄 Paginación
            document.addEventListener('click', (e) => {
                const link = e.target.closest('.pagination a');
                if (!link) return;

                e.preventDefault();

                const url = new URL(link.href);
                const page = url.searchParams.get('page');

                cargarTabla(inputBuscar.value.trim(), page);
            });

        });
    </script>

